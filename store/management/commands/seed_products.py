"""
Management command: seed_products
Copies product images from static/images/ to media/products/ and creates
Product DB records for all original Malola catalogue items.

Usage:
    python manage.py seed_products
    python manage.py seed_products --clear   # delete existing products first
"""
import shutil
from pathlib import Path
from django.core.management.base import BaseCommand
from django.conf import settings
from store.models import Product, Category

STATIC_IMG = settings.BASE_DIR / 'static' / 'images'
MEDIA_PROD = settings.MEDIA_ROOT / 'products'


def _copy(src_name):
    """Copy src image to media/products/ and return the relative media path."""
    src = STATIC_IMG / src_name
    if not src.exists():
        return ''
    dest = MEDIA_PROD / src_name
    MEDIA_PROD.mkdir(parents=True, exist_ok=True)
    if not dest.exists():
        shutil.copy2(src, dest)
    return f'products/{src_name}'


