class Command(BaseCommand):
    help = 'Seed all original Malola catalogue products into the database'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Delete all existing products before seeding',
        )

    def handle(self, *args, **options):
        if options['clear']:
            count = Product.objects.count()
            Product.objects.all().delete()
            self.stdout.write(self.style.WARNING(f'Deleted {count} existing products.'))

        created = 0
        skipped = 0

        for data in PRODUCTS:
            if Product.objects.filter(slug=data['slug']).exists():
                self.stdout.write(f"  SKIP  {data['title']} (already exists)")
                skipped += 1
                continue

            image_path = _copy(data['image_file'])
            if not image_path:
                self.stdout.write(
                    self.style.WARNING(f"  WARN  {data['title']} — image not found: {data['image_file']}")
                )

            Product.objects.create(
                title=data['title'],
                slug=data['slug'],
                category=data['category'],
                price=data['price'],
                image=image_path,
                card_bg=data.get('card_bg', '#f4f7ff'),
                badge=data.get('badge', ''),
                badge_color=data.get('badge_color', '#1565C0'),
                short_description=data.get('short_description', ''),
                description=data.get('short_description', data['title']),
                product_type=data.get('product_type', ''),
                weights=data.get('weights', '100g'),
                ingredients=data.get('ingredients', ''),
                nut_calories=data.get('nut_calories', ''),
                nut_protein=data.get('nut_protein', ''),
                nut_fat=data.get('nut_fat', ''),
                nut_carbs=data.get('nut_carbs', ''),
                nut_fibre=data.get('nut_fibre', ''),
                stock_quantity=data.get('stock_quantity', 50),
                country_of_origin=data.get('country_of_origin', 'India'),
                shelf_life=data.get('shelf_life', '6 months'),
                is_active=True,
            )
            self.stdout.write(self.style.SUCCESS(f"  CREATE {data['title']}"))
            created += 1

        self.stdout.write('')
        self.stdout.write(self.style.SUCCESS(
            f'Done — {created} products created, {skipped} skipped (already existed).'
        ))
